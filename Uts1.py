a = int(input("Masukan nilai A : "))
b = int(input("Masukan nilai B : "))

tambah = a + b 
hasil_pertambah = tambah
kurang = a - b 
hasil_pengurang = kurang
perkalian = a * b 
hasil_perkalian = perkalian
print("=======================================")
print(f"Hasil penambahan : {hasil_pertambah}")
print(f"Hasil pengurangan : {hasil_pengurang}")
print(f"Hasil perkalian : {hasil_perkalian}")